export function getLang() {
  try {
    return localStorage.getItem('sh_lang') || 'en'
  } catch {
    return 'en'
  }
}

export function setLang(lang) {
  try {
    localStorage.setItem('sh_lang', lang)
    window.location.reload()
  } catch {
    window.location.reload()
  }
}

export function useT() {
  const lang = getLang()
  return t[lang] || t.en
}

